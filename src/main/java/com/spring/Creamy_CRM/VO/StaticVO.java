package com.spring.Creamy_CRM.VO;

public class StaticVO {

	
	private ReservationVO reservationvo;
	private HostVO hostvo;
	private EmployeeVO employeevo;
	
	// getter & setter
	public ReservationVO getReservationvo() {
		return reservationvo;
	}
	public void setReservationvo(ReservationVO reservationvo) {
		this.reservationvo = reservationvo;
	}
	public HostVO getHostvo() {
		return hostvo;
	}
	public void setHostvo(HostVO hostvo) {
		this.hostvo = hostvo;
	}
	public EmployeeVO getEmployeevo() {
		return employeevo;
	}
	public void setEmployeevo(EmployeeVO employeevo) {
		this.employeevo = employeevo;
	}
	
	
	
	
}
